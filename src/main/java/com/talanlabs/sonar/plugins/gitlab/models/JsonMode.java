package com.talanlabs.sonar.plugins.gitlab.models;

public enum JsonMode {

    NONE, CODECLIMATE, SAST;

    public static JsonMode of(String name) {
        for (JsonMode m : values()) {
            if (m.name().equals(name)) {
                return m;
            }
        }
        return null;
    }

}
